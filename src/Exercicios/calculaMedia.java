package Exercicios;

public class calculaMedia {
    public static void main(String[] args) {
        double nota1 = 10d;
        double nota2 = 8.5d;
        double nota3 = 9d;
        double nota4 = 6.9d;
        double media = (nota1 + nota2 + nota3 + nota4) / 4;
        System.out.println("A média das notas é: " + media);
    }
}
